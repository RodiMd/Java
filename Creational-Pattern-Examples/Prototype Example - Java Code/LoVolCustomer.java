/**
 * Customers who buy a relatively small amount (see requirements) 
 */
class LoVolCustomer extends Customer implements Cloneable
{

/*********************************************************************************
 */
public LoVolCustomer()
/*********************************************************************************/
{	super();
}

/*********************************************************************************
 */
public LoVolCustomer( String aCompanyName )
/*********************************************************************************/ 
{	super( aCompanyName );
}

/*********************************************************************************
 */
public Object clone()
/*********************************************************************************/
{	return new LoVolCustomer( employerName );
}

/*********************************************************************************
 */
public String getType()
/*********************************************************************************/
{	return "Low volume";
}

}
