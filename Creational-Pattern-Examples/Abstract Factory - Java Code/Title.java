/**
 * The title of a document (one per document.)
 */

public abstract class Title implements Displayable
{
	public String value = null;

/*****************************************************************************************************
 */
public Title() 
/*****************************************************************************************************/ 
{	super();
}

/*****************************************************************************************************
 */
public Title( String aString)
/*****************************************************************************************************/
{
	this();
	value = aString;
}

/*****************************************************************************************************
 */
public void setValue(String aString)
/*****************************************************************************************************/
{	this.value = aString;	
}

/*****************************************************************************************************
 */
public void display()
/*****************************************************************************************************/
{	System.out.println( value );	
}

}