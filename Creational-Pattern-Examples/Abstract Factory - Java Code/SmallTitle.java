/**
 * Lower case version of title
 */
public class SmallTitle extends Title
{

/*****************************************************************************************************
 */
public SmallTitle()
/*****************************************************************************************************
 */
{	super();
}

/*****************************************************************************************************
 */
public SmallTitle( String aString)
/*****************************************************************************************************/
{	super( aString );
}

/*****************************************************************************************************
 * Precondition: 'value' != null
 * Postcondition: 'value' has been output to the console
 */
public void display()
/*****************************************************************************************************/
{	System.out.println( value.toLowerCase() + ":" );	
}

}