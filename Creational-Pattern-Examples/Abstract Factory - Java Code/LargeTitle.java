/**
 * Upper case version of title
 */

public class LargeTitle extends Title
{

/*****************************************************************************************************
 */
public LargeTitle()
/*****************************************************************************************************/
{	super();
}

/*****************************************************************************************************
 */ 
public LargeTitle( String aString)
/*****************************************************************************************************/
{	super( aString );
}

/*****************************************************************************************************
 * Precondition: 'value' != null
 * Postcondition: 'value' has been displayed at the console in upper case.
 */
public void display()
/*****************************************************************************************************/
{
	System.out.println( "\t----- Title: " + value.toUpperCase() + " -----" );	
}

}