/**
 * Expresses style of document.
 * Design note: Application of Abstract Factory design pattern.  This is the base Abstract Factory class
 */
public abstract class Style  
{	

/*****************************************************************************************************
 */
public Style()
/*****************************************************************************************************/
{	super();
}

/*****************************************************************************************************
 */
public abstract Heading getAHeading( String aString, int anIndex );
/*****************************************************************************************************/

/*****************************************************************************************************
 */
public abstract Title getATitle( String aString );
/*****************************************************************************************************/

}
