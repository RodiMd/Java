/**
 * A heading of a document (one for each section).
 */

public abstract class Heading implements Displayable
{
	protected String value = null;   // text of this heading
	protected int index = 0;   // heading number

/*****************************************************************************************************
 */
public abstract void display();
/*****************************************************************************************************/
	
/*****************************************************************************************************
 */
public Heading()
/*****************************************************************************************************/ 
{	super();
}

/*****************************************************************************************************
 */
public void setValue(String aString)
/*****************************************************************************************************/
{	this.value = aString;	
}

/*****************************************************************************************************
 */
public Heading( String aString, int aPositionInTheListOfHeadings )
/*****************************************************************************************************/
{
	this();
	this.value = aString;
	this.index = aPositionInTheListOfHeadings;
}

}